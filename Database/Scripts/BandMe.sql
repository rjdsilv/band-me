DROP TABLE IF EXISTS TBBM_USER;

CREATE TABLE IF NOT EXISTS TBBM_USER (
  USER_ID
    NUMERIC(15,0)
    NOT NULL
    PRIMARY KEY
    AUTO_INCREMENT

, EMAIL
    VARCHAR(100)
    NOT NULL
    UNIQUE

, PASSWORD
    CHAR(60)
    NOT NULL

, FIRST_NAME
    VARCHAR(50)
    NOT NULL

, MIDDLE_NAME
    VARCHAR(50)

, LAST_NAME
    VARCHAR(100)
    NOT NULL

, BIRTH_DATE
    DATE

, LAST_LOGIN_DATE
    DATETIME

, IS_ACTIVE
    BIT
    NOT NULL

, CREATE_DATE
    DATETIME
    NOT NULL

, LAST_UPDATE_DATE
    DATETIME

, LAST_INACTIVATION_DATE
    DATETIME

, LAST_ACTIVATION_DATE
    DATETIME

, ACTIVATION_LINK
    CHAR(60)
    NOT NULL
    UNIQUE

, LINK_EXPIRATION_DATE
    DATETIME
    NOT NULL
);