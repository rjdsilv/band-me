IF (exists(SELECT 1 FROM sys.all_objects WHERE type = N'U' and name = N'TBBM_USER'))
BEGIN
  DROP TABLE TBBM_USER
END

CREATE TABLE TBBM_USER (
  USER_ID
    NUMERIC(15,0)
    NOT NULL
    PRIMARY KEY
    IDENTITY

, EMAIL
    VARCHAR(100)
    NOT NULL
    UNIQUE

, PASSWORD
    CHAR(256)
    NOT NULL

, FIRST_NAME
    VARCHAR(50)
    NOT NULL

, MIDDLE_NAME
    VARCHAR(50)

, LAST_NAME
    VARCHAR(100)
    NOT NULL

, BIRTH_DATE
    DATE

, LAST_LOGIN_DATE
    DATETIME

, IS_ACTIVE
    BIT
    NOT NULL

, CREATE_DATE
    DATETIME
    NOT NULL

, LAST_UPDATE_DATE
    DATETIME

, LAST_INACTIVATION_DATE
    DATETIME

, LAST_ACTIVATION_DATE
    DATETIME
)